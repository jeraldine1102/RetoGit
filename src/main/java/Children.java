public class Children {
    private int sons;
    private String name_sons;
    private int age_sons;

    public Children(int sons, String name_sons, int age_sons) {
        this.sons = sons;
        this.name_sons = name_sons;
        this.age_sons = age_sons;
    }

    public int getSons() {
        return sons;
    }

    public String getName_sons() {
        return name_sons;
    }

    public int getAge_sons() {
        return age_sons;
    }
}
